int ParseTrade(const std::string& trade, const std::string& param)
{
	//totalRows = 0;
	vec_trade.clear();
	vector<shared_ptr<Trade>> trades;
	//Transaction transaction;
	tinyxml2::XMLDocument xmlDoc;
	auto error = xmlDoc.Parse(trade.c_str(), trade.length());
	int maxRow = 0;

	std::string trade_Code = CommonUtil::parse_value(param, "Trade_Code_Cd");

	tinyxml2::XMLElement *root = xmlDoc.FirstChildElement("PRT");
	assert(root);
	tinyxml2::XMLElement *child1 = root->FirstChildElement("Title");
	pval = child1->GetText();
	if(pval)
	{
		title = trade_Code + " " + pval;
	}

	tinyxml2::XMLElement *child2 = root->FirstChildElement("Items");
	tinyxml2::XMLElement *child21 = child2->FirstChildElement("Item");

	while (child21)
	{
		Trade trade;

		pval = child21->FirstChildElement("RowNum")->GetText();
		if (pval){
			trade.row = atoi(pval);
			maxRow = atoi(pval);
		}
		pval = child21->FirstChildElement("ColNum")->GetText();
		if (pval){
			trade.col = atoi(pval);
		}
		pval = child21->FirstChildElement("Style")->GetText();
		if (pval){
			trade.style = atoi(pval);
		}
		if (child21->FirstChildElement("ColTotal"))
		{
			pval = child21->FirstChildElement("ColTotal")->GetText();
			if (pval){
				trade.colTotal = atoi(pval);
			}
		}
		pval = child21->FirstChildElement("Label")->GetText();
		if (pval){
			trade.label = pval;
		}
		pval = child21->FirstChildElement("Value")->GetText();
		if (pval){
			trade.value = pval;
		}
		if (child21->FirstChildElement("Keyword"))
		{
			pval = child21->FirstChildElement("Keyword")->GetText();
			if (pval){
				trade.keyword = atoi(pval);
			}
		}

		//if(child21->FirstChildElement("RowNum"))
		//trade.row = atoi(child21->FirstChildElement("RowNum")->GetText());
		//if(child21->FirstChildElement("ColNum"))
		//trade.col = atoi(child21->FirstChildElement("ColNum")->GetText());
		//if(child21->FirstChildElement("Style"))
		//trade.style = atoi(child21->FirstChildElement("Style")->GetText());
		//if(child21->FirstChildElement("ColTotal"))
		//trade.colTotal = atoi(child21->FirstChildElement("ColTotal")->GetText());
		//if(child21->FirstChildElement("Label"))
		//trade.label = child21->FirstChildElement("Label")->GetText();
		//if(child21->FirstChildElement("Value"))
		//trade.value = child21->FirstChildElement("Value")->GetText();

		vec_trade.push_back(trade);
		child21 = child21->NextSiblingElement();

	}

	if (vec_trade.size() > 0)
		std::sort(vec_trade.begin(), vec_trade.end(), [](const Trade& left, const Trade& right)->bool
	{
		if (left.row == right.row)
			return left.col < right.col;
		else
			return left.row < right.row;
	});

	//int i = 0;

	int colsnum = 3;
	vec_trade[0].colTotal = colsnum;
	for(size_t j=1;j< vec_trade.size();++j)
	{
		auto it = &vec_trade.at(j);
		if (it->col == 0)
		{
			if (it->colTotal > 0)
			{
				colsnum = it->colTotal;
				it->colTotal = 1;
			}
		}
		it->colTotal = colsnum;
	}
	//DataRow dataRow;
	//dataRow.maxRows = maxRow;
	//dataRow.trades = trades;
	return maxRow;
}

string now()
{
	SYSTEMTIME time;
	GetLocalTime(&time);
	stringstream ss;
	ss << time.wYear << "-"
		<< time.wMonth << "-"
		<< time.wDay << " "
		<< time.wHour << ":"
		<< time.wMinute << ":"
		<< time.wSecond;
	return ss.str();
}

