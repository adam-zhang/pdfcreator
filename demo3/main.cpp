static vector<shared_ptr<Trade>> convert(const vector<Trade>& input)
{
	vector<shared_ptr<Trade>> vec;
	for_each(input.begin(), input.end(), [&vec](const Trade& trade)
	{
		auto p = make_shared<Trade>(trade);
		vec.push_back(p);
	});
	return vec;
}

